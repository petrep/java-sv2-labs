package math.math;

public class MathMethods {
	public static void main(String[] args) {
		System.out.println(Math.max(2, 1));
		System.out.println(Math.min(2, 1));
		System.out.println(Math.PI);
		System.out.println(Math.round(Math.PI));
		System.out.println(Math.round(2.154));
		System.out.println(Math.abs(-11.4));
		System.out.println(Math.negateExact(1));
		System.out.println(Math.addExact(1, 2));
		System.out.println(Math.subtractExact(2, 1));
		System.out.println(Math.multiplyExact(1, 2));
		System.out.println(Math.pow(1.0, 2.0));
		System.out.println(Math.incrementExact(1));
		System.out.println(Math.decrementExact(2));
		System.out.println(Math.random());
	}
}
